    private static final Map<Character, Character> S2T;
    private static final Map<Character, Character> T2S;
    
    // 设备是否为繁体中文环境
    private static final boolean IS_TRADITIONAL_CHINESE;

    static {
        String country = Locale.getDefault().getCountry();
        IS_TRADITIONAL_CHINESE = "TW".equals(country) || "HK".equals(country) || "MO".equals(country);
        
        int lenT = UTF8T.length();
        int lenS = UTF8S.length();
        
        // 安全检查：映射表长度必须一致，否则会导致字符映射错位
        if (lenT != lenS) {
            throw new IllegalStateException(
                    "简繁映射表长度不一致！UTF8T=" + lenT + ", UTF8S=" + lenS + 
                    "。这会导致字符映射错位，请检查映射表文件。");
        }
        
        int len = lenT;
        int capacity = (int) (len / 0.75f) + 1;
        S2T = new HashMap<>(capacity);
        T2S = new HashMap<>(capacity);
        for (int i = 0; i < len; i++) {
            char t = UTF8T.charAt(i);
            char s = UTF8S.charAt(i);
            S2T.put(s, t);
            T2S.put(t, s);
        }
    }

    /**
     * 判断是否需要进行简繁转换
     * 
     * 优先级：
     * 1. 如果用户强制设置了转换（KEY_FORCE_TRANS），按用户设置
     * 2. 否则默认禁用本地转换（因为弹弹play API已通过chConvert处理）
     * 3. 仅在用户未设置且确实需要时才使用设备Locale作为兜底
     * 
     * @return true 如果需要进行本地转换
     */
    public static boolean needTrans() {
        // 如果用户强制设置了，按设置来
        if (Init.contains(KEY_FORCE_TRANS)) {
            return Init.getBoolean(KEY_FORCE_TRANS, DEFAULT_FORCE_TRANS);
        }
        // 默认禁用本地转换，因为API已处理
        return false;
    }

    /**
     * 设置强制转换模式
     * 
     * @param force true=强制本地转换，false=禁用本地转换（默认）
     */
    public static void setForceTrans(boolean force) {
        Init.put(KEY_FORCE_TRANS, force);
    }

    /**
     * 清除强制转换设置，恢复默认行为（禁用本地转换）
     */
    public static void clearForceTrans() {
        Init.remove(KEY_FORCE_TRANS);
    }

    /**
     * 检查是否被强制设置
     */
    public static boolean isForcedTrans() {
        return Init.contains(KEY_FORCE_TRANS);
    }

    /**
     * 简转繁
     * @param text 输入文本
     * @return 繁体中文文本
     */
    public static String s2t(String text) {
        if (text == null) return null;
        return convert(text, S2T);
    }

    /**
     * 繁转简
     * @param text 输入文本
     * @return 简体中文文本
     */
    public static String t2s(String text) {
        if (text == null) return null;
        return convert(text, T2S);
    }

    /**
     * 转换为简体（根据设置自动判断是否转换）
     * 
     * @param text 输入文本
     * @return 简体中文文本
     */
    public static String toSimplified(String text) {
        if (text == null) return null;
        if (!needTrans()) return text;
        return t2s(text);
    }

    /**
     * 转换为繁体（根据设置自动判断是否转换）
     * 
     * @param text 输入文本
     * @return 繁体中文文本
     */
    public static String toTraditional(String text) {
        if (text == null) return null;
        if (!needTrans()) return text;
        return s2t(text);
    }

    private static String convert(String text, Map<Character, Character> map) {
        if (text == null || text.isEmpty()) return text;
        int length = text.length();
        StringBuilder sb = new StringBuilder(length);
        for (int i = 0; i < length; i++) {
            char c = text.charAt(i);
            sb.append(map.getOrDefault(c, c));
        }
        return sb.toString();
    }
